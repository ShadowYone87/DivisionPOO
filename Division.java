// Se declara la clase
public class Division {
    // Se declaran las variables
    int a, b, r;

    // Se declara el metodo
    public int dividir(int a, int b) {
        // Se llama al objeto
        r = this.a / this.b;
       
        return r;
        // Se cierra el metodo dividir
    }
    // Se cierra la clase
}
